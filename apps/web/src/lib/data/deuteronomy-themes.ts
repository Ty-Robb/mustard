export function getDeuteronomyChapterTheme(chapter: number): string {
  const theme = deuteronomyChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.theme : `Chapter ${chapter}`;
}

export function getDeuteronomyChapterKeyTopics(chapter: number): string[] {
  const theme = deuteronomyChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.keyTopics : [];
}


export function getNumbersChapterTheme(chapter: number): string {
  const theme = numbersChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.theme : `Chapter ${chapter}`;
}

export function getNumbersChapterKeyTopics(chapter: number): string[] {
  const theme = numbersChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.keyTopics : [];
}


export function getExodusChapterTheme(chapter: number): string {
  const theme = exodusChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.theme : `Chapter ${chapter}`;
}

export function getExodusChapterKeyTopics(chapter: number): string[] {
  const theme = exodusChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.keyTopics : [];
}

export function getExodusVerseThemes(chapter: number, verse: number): string[] {
  const chapterTopics = getExodusChapterKeyTopics(chapter);
  
  // Special cases for well-known verses
  const verseRef = `${chapter}:${verse}`;
  const specificThemes: { [key: string]: string[] } = {
    "3:14": ["I AM", "God's name", "self-existence", "eternal"],
    "12:13": ["Passover", "blood", "salvation", "protection"],
    "14:14": ["Lord fights", "be still", "deliverance", "trust"],
    "15:26": ["Jehovah Rapha", "healer", "obedience", "health"],
    "20:3": ["no other gods", "first commandment", "monotheism", "loyalty"],
    "20:8": ["Sabbath", "rest", "holy", "creation pattern"],
    "33:14": ["God's presence", "rest", "promise", "guidance"],
    "34:6-7": ["God's character", "merciful", "gracious", "slow to anger"]
  };
  
  if (specificThemes[verseRef]) {
    return [...new Set([...specificThemes[verseRef], ...chapterTopics.slice(0, 3)])];
  }
  
  return chapterTopics.slice(0, 3);
}


    "9:6": ["image of God", "sanctity of life", "capital punishment", "justice"],
    "12:1-3": ["Abrahamic covenant", "blessing", "great nation", "all families blessed"],
    "15:6": ["faith", "righteousness", "justification", "belief"],
    "17:1": ["El Shaddai", "walk before me", "blameless", "covenant"],
    "22:14": ["Jehovah Jireh", "Lord provides", "mount of the Lord", "provision"],
    "28:12": ["Jacob's ladder", "heaven and earth", "angels", "divine connection"],

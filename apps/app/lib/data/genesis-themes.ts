export function getGenesisChapterTheme(chapter: number): string {
  const theme = genesisChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.theme : `Chapter ${chapter}`;
}

export function getGenesisChapterKeyTopics(chapter: number): string[] {
  const theme = genesisChapterThemes.find(t => t.chapter === chapter);
  return theme ? theme.keyTopics : [];
}

